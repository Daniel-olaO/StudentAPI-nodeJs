const { Schema } = require('mongoose');
const connection = require('../index');

const UserSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
});

module.exports = connection.model('User', UserSchema);